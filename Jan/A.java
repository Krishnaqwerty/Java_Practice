package mypack;

public class A {

   

    public static void add(int a, int b){
        System.out.println(a + " + " + b + " = " + (a+b));
    }
    

    public static void sub(int a, int b){
        System.out.println(a + " - " + b + " = " + (a-b));
    }

    public static void mul(int a, int b){
        System.out.println(a + " * " + b + " = " + (a*b));

    }

    public static void div(int a, int b){
        System.out.println(a + " / " + b + " = " + (a/b));
    }

    public static void mod(int a, int b){
        System.out.println(a + " % " + b + " = " + (a%b));
    }
}
